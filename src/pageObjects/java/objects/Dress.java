package objects;

public class Dress {

    public String color;
    public String size;

    public void setDress(String color, String size){
        this.color = color;
        this.size = size;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getSize() { return size; }

    public void setSize(String size) { this.size = size; }

}
